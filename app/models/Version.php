<?php

/**
 * A Package's Version
 */
class Version extends Eloquent
{
	////////////////////////////////////////////////////////////////////
	////////////////////////////// ATTRIBUTES //////////////////////////
	////////////////////////////////////////////////////////////////////

	/**
	 * Get the keywords of a Version
	 *
	 * @return array
	 */
	public function getKeywordsAttribute()
	{
		return json_decode($this->getOriginal('keywords'), true);
	}
}
